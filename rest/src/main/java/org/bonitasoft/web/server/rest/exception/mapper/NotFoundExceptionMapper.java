package org.bonitasoft.web.server.rest.exception.mapper;

import javax.ws.rs.core.Response;
import javax.ws.rs.core.Response.Status;
import javax.ws.rs.ext.ExceptionMapper;
import javax.ws.rs.ext.Provider;

import org.bonitasoft.engine.exception.NotFoundException;
import org.bonitasoft.web.server.rest.exception.ErrorMessage;

@Provider
public class NotFoundExceptionMapper implements ExceptionMapper<NotFoundException> {

    @Override
    public Response toResponse(NotFoundException exception) {
        return Response.status(Status.NOT_FOUND).entity(new ErrorMessage(exception).withStatus(Status.NOT_FOUND)).build();
    }
}
